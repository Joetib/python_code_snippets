# To find the mode of a list, we can use the `statistics` module in Python.
# The `statistics` module provides functions to perform mathematical statistics on numeric data.

# Let's import the `statistics` module
import statistics

# Let's define a list of numbers
numbers = [1, 2, 3, 4, 4, 5, 5, 5, 6]

# Let's find the mode of the list
mode = statistics.mode(numbers)

# Let's output the mode
print(mode)


