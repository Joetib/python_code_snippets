# We can use the built-in `hex()` function in Python to convert a decimal number to hexadecimal.
# The `hex()` function takes an integer as input and returns a string representing the hexadecimal value.

decimal_number = int(input("Enter a decimal number: "))
hexadecimal_number = hex(decimal_number)

print(hexadecimal_number)

# Example:
# Input: 255
# Output: 0xff
