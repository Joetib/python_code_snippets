# To find the index of the first occurrence of an element in a list,
# we can use the `index()` method of the list.

# Let's say we have a list called `my_list` and we want to find the
# index of the first occurrence of the element `element_to_find`.

my_list = [1, 2, 3, 4, 5, 4, 3, 2, 1]
element_to_find = 4

# We can use the `index()` method to find the index of the first
# occurrence of `element_to_find` in `my_list`.
index = my_list.index(element_to_find)

# Let's output the index
print(index)

# Output
# >> 3
