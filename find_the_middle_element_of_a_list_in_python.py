# To find the middle element of a list in Python, we can use the following steps:

# 1. Get the length of the list using the len() function.
# 2. Divide the length by 2 to get the index of the middle element.
# 3. Use list indexing to access the middle element.

# Let's see the code:

my_list = [1, 2, 3, 4, 5, 6, 7]

middle_index = len(my_list) // 2
middle_element = my_list[middle_index]

print(middle_element)

# Output:
# 4
