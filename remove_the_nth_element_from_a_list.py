# To remove the nth element from a list, we can use the `del` statement.
# The `del` statement is used to delete an element from a list based on its index.

# Let's say we have a list called `my_list` and we want to remove the element at index `n`.
# We can use the following code:

del my_list[n]

# This will remove the element at index `n` from the list.

# Note: The index of the first element in a list is 0, so if you want to remove the first element, you would use `n = 0`.

# Example:
# Let's say we have a list called `numbers` with elements [1, 2, 3, 4, 5].
# If we want to remove the element at index 2 (which is 3), we can use the following code:

numbers = [1, 2, 3, 4, 5]
n = 2
del numbers[n]

# After executing this code, the list `numbers` will be [1, 2, 4, 5], with the element 3 removed.
