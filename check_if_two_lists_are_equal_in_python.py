# To check if two lists are equal in Python, we can use the '==' operator.
# It compares the elements of the lists and returns True if they are equal.

list1 = [1, 2, 3]
list2 = [1, 2, 3]

if list1 == list2:
    print("The lists are equal")
else:
    print("The lists are not equal")

# Output
# >> The lists are equal
