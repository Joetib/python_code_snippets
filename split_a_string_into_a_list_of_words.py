# To split a string into a list of words, we can use the `split()` method.
# The `split()` method splits a string into a list of substrings based on a delimiter.

# Example:
string = "Hello, how are you?"
words = string.split()
print(words)

# Output:
# ['Hello,', 'how', 'are', 'you?']
