# To swap two elements in a list, you can use the following code:

# Define a list
my_list = [1, 2, 3, 4, 5]

# Define the indices of the elements you want to swap
index1 = 1
index2 = 3

# Swap the elements using a temporary variable
temp = my_list[index1]
my_list[index1] = my_list[index2]
my_list[index2] = temp

# Print the updated list
print(my_list)

# Output
# [1, 4, 3, 2, 5]
