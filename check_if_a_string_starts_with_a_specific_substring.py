# We can use the `startswith()` method of a string to check if it starts with a specific substring.
# The `startswith()` method returns `True` if the string starts with the specified substring, and `False` otherwise.

string = "Hello, world!"
substring = "Hello"

if string.startswith(substring):
    print("The string starts with the specified substring")
else:
    print("The string does not start with the specified substring")

# Output
# The string starts with the specified substring
