# We can use the math module to calculate the greatest common divisor (GCD) of two numbers.
# The math module provides a function called gcd() that takes two arguments and returns their GCD.
# Let's import the math module and calculate the GCD of two numbers.

import math

num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

gcd = math.gcd(num1, num2)

print("The greatest common divisor is:", gcd)


