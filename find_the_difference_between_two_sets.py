# We can find the difference between two sets using the '-' operator in Python.
# The '-' operator returns a new set that contains elements that are in the first set but not in the second set.

set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

difference = set1 - set2

print(difference)

# Output:
# {1, 2, 3}
