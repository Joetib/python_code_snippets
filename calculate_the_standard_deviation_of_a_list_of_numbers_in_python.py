# To calculate the standard deviation of a list of numbers in Python, we can use the statistics module which is part of the Python standard library.

import statistics

# Example list of numbers
numbers = [1, 2, 3, 4, 5]

# Calculate the standard deviation
std_dev = statistics.stdev(numbers)

# Print the result
print(std_dev)

# Output
# >> 1.5811388300841898
