# To find the maximum value in a list, we can use the built-in max() function in Python.
# The max() function returns the largest item in an iterable or the largest of two or more arguments.

numbers = [5, 10, 3, 8, 2]

# Find the maximum value in the list
maximum = max(numbers)

print(maximum)

# Output
# >> 10
