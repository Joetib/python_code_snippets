# To find the sum of all elements in a list, we can use the built-in `sum()` function in Python.
# The `sum()` function takes an iterable (such as a list) as its argument and returns the sum of all elements.

my_list = [1, 2, 3, 4, 5]
# Let's calculate the sum of all elements in the list
total_sum = sum(my_list)
print(total_sum)

# Output
# >> 15
