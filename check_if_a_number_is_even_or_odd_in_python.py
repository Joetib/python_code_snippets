# To check if a number is even or odd in Python, we can use the modulo operator (%).
# If a number is divisible by 2 without any remainder, it is even. Otherwise, it is odd.

number = int(input("Enter a number: "))

# Check if the number is even
if number % 2 == 0:
    print("Even")
else:
    print("Odd")

